# 演算
x = 10
y = 5
print(x + y)  # 結果: 15
print(x - y)  # 結果: 5
print(x * y)  # 結果: 50
print(x / y)  # 結果: 2.0
print(x % y)  # 結果: 0 (剰余)

z = 2 ** 3  # 2の3乗
print(z)  # 結果: 8

a = 10
b = 5
print(a > b)   # 結果: True
print(a == b)  # 結果: False

x = True
y = False
print(x and y)  # 結果: False
print(x or y)   # 結果: True
print(not x)    # 結果: False

x = "123"
y = int(x)  # 文字列から整数に変換
print(y)    # 結果: 123

z = 3.14
int_z = int(z)  # 小数点以下は切り捨て
print(int_z)    # 結果: 3

result = (2 + 3) * 4  # 結果: 20
print(result)
